--------------------------------------------------------

  CREATE OR REPLACE TRIGGER /*"PMO".*/"PMO_TRG_PMO_GESTION_DEMANDA" 
before  update
ON PMO_TOTAL_PROYECTOS
FOR EACH ROW
BEGIN
 -- ACTUALIZA REQ PADRE
 if :NEW.NO_REQ_PADRE!=:OLD.NO_REQ_PADRE then
 update pmo_gestion_demanda set no_req_padre=:NEW.NO_REQ_PADRE where id_proyecto=:OLD.ID_PROYECTOS;
 end if;
  if :NEW.id_estado_priorizacion=3 then
 update pmo_gestion_demanda set estado_general='CANCELADO' where id_proyecto=:OLD.ID_PROYECTOS;
 end if;
  if :NEW.id_estado_priorizacion=4 then
 update pmo_gestion_demanda set estado_general='ON HOLD' where id_proyecto=:OLD.ID_PROYECTOS;
 end if;
  if :NEW.id_estado_priorizacion=1 then
 update pmo_gestion_demanda set estado_general='POR PROGRAMAR' where id_proyecto=:OLD.ID_PROYECTOS;
 end if;
 if :NEW.id_estado_priorizacion=5 then
 update pmo_gestion_demanda set estado_general='EN CURSO' where id_proyecto=:OLD.ID_PROYECTOS and estado_general='PENDIENTE';
 end if;
if :NEW.ID_PM!=:OLD.ID_PM then
 update pmo_gestion_demanda set PM_ID=:NEW.ID_PM where id_proyecto=:OLD.ID_PROYECTOS;
 update pmo_gestion_documentacion set ID_pm=:NEW.ID_PM where id_proyectos=:OLD.ID_PROYECTOS;
 end if;
if :NEW.priorizacion!=:OLD.priorizacion then
 update pmo_gestion_demanda set priorizacion=:NEW.priorizacion where id_proyecto=:OLD.ID_PROYECTOS;
 end if; 
 if :NEW.numero_priorizacion!=:OLD.numero_priorizacion then
 update pmo_gestion_demanda set numero_priorizacion=:NEW.numero_priorizacion where id_proyecto=:OLD.ID_PROYECTOS;
 end if;
 
 
END;
/
ALTER TRIGGER /*"PMO".*/"PMO_TRG_PMO_GESTION_DEMANDA" ENABLE;
